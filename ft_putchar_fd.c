#include "libft.h"

void	ft_putchar_fd(char c, int fd)
{
	if (fd < 0)
		return ;
	write(fd, &c, 1);
}

/*
c: The character to output.
fd: The file descriptor on which to write.
Outputs the character ’c’ to the given file
descriptor. Verifies valid fd.
*/
