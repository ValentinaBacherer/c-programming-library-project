#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char		*d;
	const unsigned char	*s;

	d = (unsigned char *)dst;
	s = (const unsigned char *)src;
	if (len == 0 || dst == src)
		return (dst);
	if (s < d)
	{
		while (len--)
			d[len] = s[len];
		return (dst);
	}
	while (len--)
		*d++ = *s++;
	return (dst);
}
/*
- in first loop len-- becomes an index.
- (s < d) means that the source position in memory is before,
we copy backguards.
- otherwise starts forward copying.
*/